    private void btnEscribirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEscribirActionPerformed
        // TODO add your handling code here:
        String apellidos = txtApellidos.getText();
        String nombres = txtNombres.getText();
        String codigo = txtCodigo.getText();
        // Declarar un objeto
        Trabajador trabajador = new Trabajador();
        trabajador.setApellidos(apellidos);
        trabajador.setNombres(nombres);
        trabajador.setCodigo(codigo);
        // Enviar mensaje de confirmaci√≥n
        lblMensaje.setText("Se escribio los atributos del objeto trabajador");
    }//GEN-LAST:event_btnEscribirActionPerformed

